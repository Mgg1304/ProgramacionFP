package Ejer01;

public class Lib_ejer1 {
	
	/**
	 * Imprime cual es el perimetro total del cuadrado
	 * @param lado: entero
	 */

	public static void calcularPerimetroCuadrado(int lado) {
		int valor = lado *4;
		System.out.println("Perimetro: " + valor);
	}
	
	/**
	 * Imprime cual es la superficie del cuadrado
	 * @param lado: entero
	 */
	public static void calcularSuperficie(int lado) {
		int valor = lado * lado;
		System.out.println("Perimetro: " + valor);
	}
}
