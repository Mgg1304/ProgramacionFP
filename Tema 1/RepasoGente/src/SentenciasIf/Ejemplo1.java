package SentenciasIf;

import java.util.Scanner;

public class Ejemplo1 {

	public static Scanner sc = new Scanner(System.in);

	/**
	 * La intrucción IF busca ejecutar una serie de instrucciones si se cumple una
	 * condición establecidad.
	 */

	public static void main(String[] args) {

		int x = 0, y = 1;

		// Esta condición se cumplirá, entonces se imprimira la información que esta
		// escrita
		if (x == 0) {
			System.out.println("Se cumple la condición");
		}
		if (y == 0) {
			System.out.println("Se cumple la condición");
		}

	}
}
